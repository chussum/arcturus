#!/usr/bin/env bash
# One-time (idempotent) secret hardening for this machine:
#   data/env-key + data/jwt-secret (plaintext, 0600)
#     вҶ’ dotenvx-encrypted .env.secrets (repo root)
#     вҶ’ private key stored only in the macOS Keychain (service: arcturus-dotenvx)
# Existing values are migrated as-is, so encrypted DB rows and live sessions survive.
#   bun run secrets:init
set -euo pipefail
cd "$(dirname "$0")/.."

# --input: prompt for each fresh value instead of auto-generating (used by
# server-up.sh's "м§Ғм ‘ мһ…л Ҙ" menu choice). Blank input falls back to auto-generate.
INPUT_MODE=false
for arg in "$@"; do
  [ "$arg" = "--input" ] && INPUT_MODE=true
done

KEYCHAIN_SERVICE="arcturus-dotenvx"
SECRETS_FILE=".env.secrets"
DATA_DIR="${ARCTURUS_DATA_DIR:-apps/api/data}"

say()  { printf '\033[1;33mвҶ’ %s\033[0m\n' "$1"; }
ok()   { printf '\033[1;32mвң“ %s\033[0m\n' "$1"; }
warn() { printf '\033[1;31m! %s\033[0m\n' "$1"; }

if [ "$(uname)" != "Darwin" ]; then
  warn "мқҙ мҠӨнҒ¬лҰҪнҠёлҠ” macOS м „мҡ©мһ…лӢҲлӢӨ (private keyлҘј Keychainм—җ ліҙкҙҖ)."
  echo "  Linuxм—җм„ңлҠ” ARCTURUS_ENV_KEY / ARCTURUS_JWT_SECRET нҷҳкІҪ ліҖмҲҳлҘј м§Ғм ‘ мЈјмһ…н•ҳкұ°лӮҳ"
  echo "  кё°ліё нҢҢмқј fallback(data/env-keyВ·jwt-secret, 0600)мқ„ к·ёлҢҖлЎң мӮ¬мҡ©н•ҳм„ёмҡ”."
  exit 1
fi

# Prints the decrypted value of $1 using private key $2; empty when undecryptable.
# (`dotenvx get` exits 0 and echoes the ciphertext on key mismatch, so we
# compare values instead of trusting exit codes.)
decrypted() {
  local out
  out="$(DOTENV_PRIVATE_KEY_SECRETS="$2" bunx dotenvx get "$1" -f "$SECRETS_FILE" 2>/dev/null || true)"
  case "$out" in encrypted:* | '') echo '' ;; *) printf '%s' "$out" ;; esac
}

keys_file_pk() {
  [ -f .env.keys ] || { echo ''; return; }
  grep '^DOTENV_PRIVATE_KEY_SECRETS=' .env.keys | cut -d= -f2- | tr -d '"' || true
}

# 1. Locate an existing private key: keychain first, then a leftover .env.keys
#    (e.g. an interrupted earlier run).
PK="$(security find-generic-password -s "$KEYCHAIN_SERVICE" -w 2>/dev/null || true)"
[ -n "$PK" ] || PK="$(keys_file_pk)"

# 2. Current decryptable values in .env.secrets, if any. A secrets file we
#    cannot decrypt is unrecoverable from here вҖ” never overwrite it (dotenvx
#    set would reuse its public key and produce more values we can't read).
require_recoverable() { # require_recoverable <var> <decrypted value>
  grep -q "^$1=" "$SECRETS_FILE" || return 0
  [ -n "$2" ] && return 0
  warn "$SECRETS_FILE м—җ $1 мқҙ(к°Җ) мһҲм§Җл§Ң ліөнҳёнҷ”н•  нӮӨлҘј м°ҫм§Җ лӘ»н–ҲмҠөлӢҲлӢӨ (Keychain '$KEYCHAIN_SERVICE'В·.env.keys лӘЁл‘җ м—ҶмқҢ)."
  echo "  нӮӨлҘј ліөкө¬н•ҙ лӢӨмӢң мӢӨн–үн•ҳкұ°лӮҳ, нҸ¬кё°н•ҳл Өл©ҙ $SECRETS_FILE мқ„ м§Ғм ‘ мӮӯм ң нӣ„ мһ¬мӢӨн–үн•ҳм„ёмҡ”."
  exit 1
}

ENV_KEY_CUR=''
JWT_CUR=''
if [ -f "$SECRETS_FILE" ]; then
  ENV_KEY_CUR="$(decrypted ARCTURUS_ENV_KEY "$PK")"
  JWT_CUR="$(decrypted ARCTURUS_JWT_SECRET "$PK")"
  require_recoverable ARCTURUS_ENV_KEY "$ENV_KEY_CUR"
  require_recoverable ARCTURUS_JWT_SECRET "$JWT_CUR"
fi

# 3. Decide the values. Priority: already-encrypted value > legacy data file > fresh.
ENV_KEY_FILE="$DATA_DIR/env-key"
JWT_FILE="$DATA_DIR/jwt-secret"

resolve_value() { # resolve_value <current> <legacy file> <label>
  local cur="$1" file="$2" label="$3"
  if [ -n "$cur" ]; then
    say "$label: кё°мЎҙ $SECRETS_FILE к°’ мң м§Җ" >&2
    printf '%s' "$cur"
  elif [ -f "$file" ]; then
    say "$label: кё°мЎҙ $file к°’мқ„ мқҙм „ (кё°мЎҙ лҚ°мқҙн„°/м„ём…ҳ мң м§Җ)" >&2
    printf '%s' "$(cat "$file")"
  elif [ "$INPUT_MODE" = true ]; then
    # Prompt on /dev/tty so this works even though the function runs inside $(...)
    # (stdout is captured for the value). Blank вҶ’ auto-generate.
    local entered
    read -rp "$label к°’ мһ…л Ҙ (л№Ҳм№ё=мһҗлҸҷ мғқм„ұ / Enter value, blank = auto-generate): " entered </dev/tty
    if [ -n "$entered" ]; then
      say "$label: мһ…л Ҙк°’ мӮ¬мҡ©" >&2
      printf '%s' "$entered"
    else
      say "$label: мғҲлЎң мғқм„ұ" >&2
      openssl rand -hex 32
    fi
  else
    say "$label: мғҲлЎң мғқм„ұ" >&2
    openssl rand -hex 32
  fi
}

ENV_KEY_VAL="$(resolve_value "$ENV_KEY_CUR" "$ENV_KEY_FILE" "ARCTURUS_ENV_KEY")"
JWT_VAL="$(resolve_value "$JWT_CUR" "$JWT_FILE" "ARCTURUS_JWT_SECRET")"

# 4. Encrypt into .env.secrets (no-op for values already in place). A fresh
#    file gets a usage header first вҖ” dotenvx set preserves existing comments.
if [ ! -f "$SECRETS_FILE" ]; then
  cat > "$SECRETS_FILE" <<'EOF'
# мқҙ нҢҢмқјмқҖ `bun run secrets:init`мқҙ л§Ңл“Өкі  кҙҖлҰ¬н•©лӢҲлӢӨ.
# мӢңнҒ¬лҰҝ м¶”к°Җ/мҲҳм •: `KEY=нҸүл¬ё` н•ң мӨ„ м¶”к°Җ вҶ’ `bun run secrets:update` вҶ’ `bun run server:restart`
# DOTENV_PUBLIC_KEY_SECRETSлҠ” dotenvxк°Җ мһҗлҸҷ мғқм„ұн•ҳлҠ” м•”нҳёнҷ”мҡ© кіөк°ң нӮӨ вҖ” м§Ғм ‘ мһ…л Ҙн•ҳм§Җ л§Ҳм„ёмҡ”.
#
# Managed by `bun run secrets:init`. Add/change a secret: append `KEY=plainvalue`,
# then `bun run secrets:update` and `bun run server:restart`.
# DOTENV_PUBLIC_KEY_SECRETS is auto-generated by dotenvx вҖ” never set it yourself.
#
EOF
fi
[ "$ENV_KEY_CUR" = "$ENV_KEY_VAL" ] || bunx dotenvx set ARCTURUS_ENV_KEY "$ENV_KEY_VAL" -f "$SECRETS_FILE" >/dev/null
[ "$JWT_CUR" = "$JWT_VAL" ] || bunx dotenvx set ARCTURUS_JWT_SECRET "$JWT_VAL" -f "$SECRETS_FILE" >/dev/null
chmod 600 "$SECRETS_FILE"
ok "$SECRETS_FILE м•”нҳёнҷ” м ҖмһҘ мҷ„лЈҢ"

# 5. Make sure the private key is in the keychain (a fresh `dotenvx set` above
#    may have just created the keypair in .env.keys).
[ -n "$PK" ] || PK="$(keys_file_pk)"
if [ -z "$PK" ]; then
  warn "dotenvx private keyлҘј м°ҫм§Җ лӘ»н–ҲмҠөлӢҲлӢӨ вҖ” м•„л¬ҙкІғлҸ„ мӮӯм ңн•ҳм§Җ м•Ҡм•ҳмҠөлӢҲлӢӨ."
  exit 1
fi
security add-generic-password -U -s "$KEYCHAIN_SERVICE" -a "$USER" -w "$PK"
ok "private keyлҘј Keychainм—җ м ҖмһҘ (service: $KEYCHAIN_SERVICE)"

# 6. Verify a full round-trip strictly through the keychain before deleting anything.
KC_PK="$(security find-generic-password -s "$KEYCHAIN_SERVICE" -w)"
if [ "$(decrypted ARCTURUS_ENV_KEY "$KC_PK")" != "$ENV_KEY_VAL" ] ||
  [ "$(decrypted ARCTURUS_JWT_SECRET "$KC_PK")" != "$JWT_VAL" ]; then
  warn "Keychain нӮӨ round-trip кІҖмҰқ мӢӨнҢЁ вҖ” м•„л¬ҙкІғлҸ„ мӮӯм ңн•ҳм§Җ м•Ҡм•ҳмҠөлӢҲлӢӨ."
  exit 1
fi
ok "Keychain нӮӨлЎң ліөнҳёнҷ” round-trip кІҖмҰқ нҶөкіј"

# 7. Only now remove the plaintext copies.
if [ -f .env.keys ]; then
  rm .env.keys
  ok ".env.keys мӮӯм ң вҖ” private keyлҠ” мқҙм ң Keychainм—җл§Ң мЎҙмһ¬н•©лӢҲлӢӨ"
fi
remove_legacy() { # remove_legacy <file> <expected value>
  [ -f "$1" ] || return 0
  if [ "$(cat "$1")" = "$2" ]; then
    rm "$1"
    ok "$1 мӮӯм ң (к°’мқҖ $SECRETS_FILE лЎң мқҙм „лҗЁ)"
  else
    warn "$1 мқҳ к°’мқҙ $SECRETS_FILE кіј лӢ¬лқј к·ёлҢҖлЎң л‘җм—ҲмҠөлӢҲлӢӨ вҖ” м–ҙлҠҗ нӮӨлЎң м•”нҳёнҷ”лҗң лҚ°мқҙн„°мқём§Җ нҷ•мқё нӣ„ м§Ғм ‘ м •лҰ¬н•ҳм„ёмҡ”."
  fi
}
remove_legacy "$ENV_KEY_FILE" "$ENV_KEY_VAL"
remove_legacy "$JWT_FILE" "$JWT_VAL"

echo ""
ok "мҷ„лЈҢ. л°ҳмҳҒн•ҳл Өл©ҙ:  bun run server:up   (devлҠ” bun run dev к·ёлҢҖлЎң)"
echo "  В· мӢңнҒ¬лҰҝ м¶”к°Җ/мҲҳм •:  $SECRETS_FILE м—җ KEY=к°’ мқ„ м Ғкі   bun run secrets:update"
echo "  В· лӢӨлҘё лЁёмӢ мңјлЎң мқҙм „ мӢң нӮӨ л°ұм—…:  security find-generic-password -s $KEYCHAIN_SERVICE -w"
